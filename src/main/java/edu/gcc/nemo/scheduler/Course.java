package edu.gcc.nemo.scheduler;

public class Course {
    String courseCode;
    String department;
    String semester;
    String time;
    String day;
    String prof;
    String name;
    int creditHours;
    int capacity;

    //Getters and Setters
    public String getCourseCode() {
        return courseCode;
    }

    public String getDepartment() {
        return department;
    }

    public String getSemester() {
        return semester;
    }

    public String getTime() {
        return time;
    }

    public String getDay() {
        return day;
    }

    public String getProf() {
        return prof;
    }

    public String getName() {
        return name;
    }

    public int getCreditHours() {
        return creditHours;
    }

    public int getCapacity() {
        return capacity;
    }
}
